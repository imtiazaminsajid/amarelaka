package lab1.sdmgap.edu.amarelaka;

public class WardDataModel {

    private String ward_name;
    private String ward_img;
    private String ward_fireService;
    private String ward_police_station_name;
    private String ward_police_station_number;

    public String getWard_name() {
        return ward_name;
    }

    public void setWard_name(String ward_name) {
        this.ward_name = ward_name;
    }

    public String getWard_img() {
        return ward_img;
    }

    public void setWard_img(String ward_img) {
        this.ward_img = ward_img;
    }

    public String getWard_fireService() {
        return ward_fireService;
    }

    public void setWard_fireService(String ward_fireService) {
        this.ward_fireService = ward_fireService;
    }

    public String getWard_police_station_name() {
        return ward_police_station_name;
    }

    public void setWard_police_station_name(String ward_police_station_name) {
        this.ward_police_station_name = ward_police_station_name;
    }

    public String getWard_police_station_number() {
        return ward_police_station_number;
    }

    public void setWard_police_station_number(String ward_police_station_number) {
        this.ward_police_station_number = ward_police_station_number;
    }
}
